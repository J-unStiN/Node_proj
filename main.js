function hello() {
    str1 = "121212"
    str2 = "asjdkasjdk"
    console.log("Hello node"+str1+str2);

    var a = 1, b = 2, c = 3, d = 4;

    // 비교연산자
    a += 1;  //  a = a + 1; 과 같다 
    b -= 1;  //  b = b - 1; 과 같다 
    c *= 2;  //  c = c * 2; 과 같다 
    d /= 2;  //  d = d / 2; 과 같다 

    console.log(a,b,c,d)
}

hello();

